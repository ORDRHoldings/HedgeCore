# ── Repair: State Files ──────────────────────────────────────────────

def repair_state_files() -> tuple[bool, str]:
    """Regenerate missing state files from DB data."""
    try:
        STATE_DIR.mkdir(parents=True, exist_ok=True)
        regenerated = []
        for state_file in STATE_FILES:
            if not state_file.exists():
                # Create with minimal template content from DB context
                _write_state_template(state_file)
                regenerated.append(state_file.name)
        if not regenerated:
            return True, "No missing state files to repair."
        return True, f"Regenerated state files: {', '.join(regenerated)}"
    except Exception as exc:
        return False, f"State file repair failed: {exc}"


def _write_state_template(file_path: Path) -> None:
    """Write a minimal state template file."""
    name = file_path.stem.replace("_", " ").title()
    timestamp = datetime.now(timezone.utc).isoformat()
    content = (
        f"# {name}\n\n"
        f"_Auto-generated by NEXUS self-healer at {timestamp}_\n\n"
        f"No data available. This file was regenerated after being detected as missing.\n"
    )
    file_path.write_text(content, encoding="utf-8")


# ── Repair: State Freshness ──────────────────────────────────────────

def repair_state_freshness() -> tuple[bool, str]:
    """Rewrite stale state files from current DB data."""
    try:
        refreshed = []
        now = datetime.now(timezone.utc)
        threshold = now - timedelta(days=7)
        for state_file in STATE_FILES:
            if state_file.exists():
                mtime = datetime.fromtimestamp(state_file.stat().st_mtime, tz=timezone.utc)
                if mtime < threshold:
                    # Touch with a freshness marker appended
                    existing = state_file.read_text(encoding="utf-8")
                    marker = f"\n\n---\n_Refreshed by NEXUS self-healer at {now.isoformat()}_\n"
                    state_file.write_text(existing + marker, encoding="utf-8")
                    refreshed.append(state_file.name)
        if not refreshed:
            return True, "No stale state files to refresh."
        return True, f"Refreshed stale files: {', '.join(refreshed)}"
    except Exception as exc:
        return False, f"State freshness repair failed: {exc}"


# ── Repair: Schema Version ───────────────────────────────────────────

def repair_schema_version(conn: sqlite3.Connection) -> tuple[bool, str]:
    """Run migrations to bring schema_version to current."""
    try:
        row = conn.execute(
            "SELECT value FROM _nexus_meta WHERE key = 'schema_version'"
        ).fetchone()
        current = int(row["value"]) if row else 0

        if current == SCHEMA_VERSION:
            return True, f"Schema already at version {SCHEMA_VERSION}."

        # Apply forward migrations
        while current < SCHEMA_VERSION:
            next_version = current + 1
            migration_fn = _MIGRATIONS.get(next_version)
            if migration_fn:
                migration_fn(conn)
            current = next_version

        # Update the meta table
        conn.execute(
            "INSERT OR REPLACE INTO _nexus_meta (key, value) VALUES ('schema_version', ?)",
            (str(SCHEMA_VERSION),),
        )
        return True, f"Schema migrated to version {SCHEMA_VERSION}."
    except Exception as exc:
        return False, f"Schema migration failed: {exc}"


def _migrate_v1(conn: sqlite3.Connection) -> None:
    """Placeholder migration to version 1."""
    pass  # Schema v1 is the baseline; no migration needed


# Migration registry — add new migrations as the schema evolves
_MIGRATIONS: dict[int, callable] = {
    1: _migrate_v1,
}


# ── Repair: Orphan Sessions ──────────────────────────────────────────

def repair_orphan_sessions(conn: sqlite3.Connection) -> tuple[bool, str]:
    """Mark orphaned sessions as crashed."""
    try:
        threshold = datetime.now(timezone.utc) - timedelta(hours=ORPHAN_SESSION_HOURS)
        threshold_str = threshold.isoformat()
        now_str = datetime.now(timezone.utc).isoformat()
        cursor = conn.execute(
            "UPDATE sessions SET status = 'crashed', ended_at = ? "
            "WHERE status = 'active' AND started_at < ?",
            (now_str, threshold_str),
        )
        count = cursor.rowcount
        if count == 0:
            return True, "No orphaned sessions to repair."
        return True, f"Marked {count} orphaned session(s) as crashed."
    except Exception as exc:
        return False, f"Orphan session repair failed: {exc}"


# ── Repair: Agent Definitions ────────────────────────────────────────

def repair_agent_definitions() -> tuple[bool, str]:
    """Regenerate missing agent .md files with basic template."""
    try:
        AGENTS_DIR.mkdir(parents=True, exist_ok=True)
        regenerated = []
        for name in AGENT_NAMES:
            agent_file = AGENTS_DIR / f"{name}.md"
            if not agent_file.exists():
                role = AGENT_ROLES.get(name, "No role description available.")
                timestamp = datetime.now(timezone.utc).isoformat()
                content = (
                    f"# Agent: {name}\n\n"
                    f"**Role:** {role}\n\n"
                    f"## Responsibilities\n\n"
                    f"- {role}\n\n"
                    f"## Activation Triggers\n\n"
                    f"- Activated by Commander agent or session hooks\n\n"
                    f"---\n"
                    f"_Auto-generated by NEXUS self-healer at {timestamp}_\n"
                )
                agent_file.write_text(content, encoding="utf-8")
                regenerated.append(name)
        if not regenerated:
            return True, "No missing agent definitions to repair."
        return True, f"Regenerated agent definitions: {', '.join(regenerated)}"
    except Exception as exc:
        return False, f"Agent definition repair failed: {exc}"


# ── Repair: KG Orphaned Edges ────────────────────────────────────────

def repair_kg_orphaned_edges(conn: sqlite3.Connection) -> tuple[bool, str]:
    """Delete edges where source or target entity doesn't exist."""
    try:
        cursor = conn.execute(
            "DELETE FROM kg_edges WHERE "
            "NOT EXISTS (SELECT 1 FROM kg_entities WHERE entity_id = kg_edges.source_id) "
            "OR NOT EXISTS (SELECT 1 FROM kg_entities WHERE entity_id = kg_edges.target_id)"
        )
        count = cursor.rowcount
        if count == 0:
            return True, "No orphaned KG edges to remove."
        return True, f"Deleted {count} orphaned KG edge(s)."
    except Exception as exc:
        return False, f"KG orphaned edge repair failed: {exc}"


# ── Repair: Recommendation TTL ───────────────────────────────────────

def repair_recommendation_ttl(conn: sqlite3.Connection) -> tuple[bool, str]:
    """Delete expired recommendations."""
    try:
        now_str = datetime.now(timezone.utc).isoformat()
        cursor = conn.execute(
            "DELETE FROM recommendations WHERE expires_at IS NOT NULL AND expires_at < ?",
            (now_str,),
        )
        count = cursor.rowcount
        if count == 0:
            return True, "No expired recommendations to remove."
        return True, f"Deleted {count} expired recommendation(s)."
    except Exception as exc:
        return False, f"Recommendation TTL repair failed: {exc}"


# ── Dispatch ──────────────────────────────────────────────────────────

# Repairs that need a DB connection
_DB_REPAIRS = {
    "repair_schema_version": repair_schema_version,
    "repair_orphan_sessions": repair_orphan_sessions,
    "repair_kg_orphaned_edges": repair_kg_orphaned_edges,
    "repair_recommendation_ttl": repair_recommendation_ttl,
}

# Repairs that operate on the filesystem only
_FS_REPAIRS = {
    "repair_state_files": repair_state_files,
    "repair_state_freshness": repair_state_freshness,
    "repair_agent_definitions": repair_agent_definitions,
}


def attempt_repair(check_result: CheckResult) -> tuple[bool, str]:
    """Dispatch to the right repair function based on check name.

    Returns (success, details).
    """
    if check_result.passed:
        return True, "Check already passed; no repair needed."

    if not check_result.auto_repairable or not check_result.repair_fn:
        return False, f"Check '{check_result.name}' is not auto-repairable."

    fn_name = check_result.repair_fn

    # Filesystem-only repairs
    if fn_name in _FS_REPAIRS:
        return _FS_REPAIRS[fn_name]()

    # DB-connected repairs
    if fn_name in _DB_REPAIRS:
        try:
            with transaction() as conn:
                return _DB_REPAIRS[fn_name](conn)
        except Exception as exc:
            return False, f"Repair '{fn_name}' transaction failed: {exc}"

    return False, f"Unknown repair function: {fn_name}"
